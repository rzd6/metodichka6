import type { UserGender } from "@/data/roles"

/** Пары мужской → женский для типичных формулировок в докладах и /me */
const EXACT_REPLACEMENTS: [string, string][] = [
  ["Приступил", "Приступила"],
  ["приступил", "приступила"],
  ["Приступили", "Приступили"],
  ["Заступил", "Заступила"],
  ["заступил", "заступила"],
  ["Закончил", "Закончила"],
  ["закончил", "закончила"],
  ["Отправляюсь", "Отправляюсь"],
  ["Занял", "Заняла"],
  ["занял", "заняла"],
  ["Вернулся", "Вернулась"],
  ["вернулся", "вернулась"],
  ["Взял", "Взяла"],
  ["взял", "взяла"],
  ["Сдал", "Сдала"],
  ["сдал", "сдала"],
  ["вернул", "вернула"],
  ["Вернул", "Вернула"],
  ["взял паспорт", "взяла паспорт"],
  ["взял трудовую", "взяла трудовую"],
  ["взял водительское", "взяла водительское"],
  ["взял диплом", "взяла диплом"],
  ["взял медицинскую", "взяла медицинскую"],
  ["Выезжаю", "Выезжаю"],
  ["объявлен", "объявлена"],
  ["объявлен(о)", "объявлена"],
  ["находящийся", "находящаяся"],
  ["находящийся в должности", "находящаяся в должности"],
  ["прошу вас", "прошу вас"],
  ["Выполняю", "Выполняю"],
  ["выполняю", "выполняю"],
  ["Принято, выполняю", "Принято, выполняю"],
  ["Принято! Выполняю", "Принято! Выполняю"],
]

/**
 * Подставляет женские формы глаголов там, где в тексте есть мужские (доклады, /me).
 */
export function applyGenderToText(text: string, gender: UserGender): string {
  if (gender !== "female") return text

  let result = text
  for (const [male, female] of EXACT_REPLACEMENTS) {
    result = result.split(male).join(female)
  }

  // Общие окончания прошедшего времени 1-го лица: ...ил → ...ила (осторожно с короткими словами)
  result = result.replace(
    /\b([А-Яа-яЁё]{4,})(ил|ыл)\b/g,
    (match, stem, ending) => {
      if (ending === "ыл") return `${stem}ыла`
      return `${stem}ила`
    }
  )

  return result
}
