export function sortUsersByRole<T extends { role: string }>(users: T[]): T[] {
  return [...users].sort((a, b) => {
    const rankA = ROLE_RANK[a.role as UserRole] ?? 0
    const rankB = ROLE_RANK[b.role as UserRole] ?? 0
    return rankB - rankA
  })
}

